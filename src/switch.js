function show(show1) {
  document.getElementById(show1).style.display = "flex";
}
function hide(hide1) {
  document.getElementById(hide1).style.display = "none";
}
function tog(tog1) {
  document.getElementById(tog1).style.backgroundColor = "var(--color-highlight";
}
function toh(toh1) {
  document.getElementById(toh1).style.backgroundColor =
    "var(--color-foreground)";
}
